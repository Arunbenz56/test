package com.testing;

public class ForEachLoop {
	public static void main(String[] args) {
		int arr[]= {12,23,43,43};
		String str[]= {"selenium","project","training"};
		for (int num : arr) {
			System.out.println(num);
		}
		for (String string : str) {
			System.out.println(string);
		}
		
	}

}
